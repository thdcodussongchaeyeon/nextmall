import React from "react";
import Head from "next/head";
import Link from "next/link";
import { useContext, useEffect, useState } from "react";
import { Store } from "../utils/Store";

export default function Layout({ title, children }) {
  const { state } = useContext(Store);
  const { cart } = state;
  const [cartItemsCount, setCartItemsCount] = useState(0);
  useEffect(() => {
    setCartItemsCount(cart.cartItems.reduce((a, c) => a + c.quantity, 0));
  }, [cart.cartItems]);
  return (
    <>
      <Head>
        <title> {title ? title + " - NextMall" : "NextMall"} </title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="flex flex-col h-screen justify-between">
        <nav className="flex justify-between items-center bg-slate-200 h-12 px-4 shadow-md">
          <Link href="/">
            <a className="text-lg font-bold">NextMall</a>
          </Link>
          <div>
            <Link href="/cart">
              <a className="p-2">
                Cart
                {cartItemsCount > 0 && (
                  <span className="ml-1 rounded-full bg-red-600 px-2 py-1 text-xs font-bold text-white">
                    {cartItemsCount}
                  </span>
                )}
              </a>
            </Link>
            <Link href="/login">
              <a className="p-2">Login</a>
            </Link>
          </div>
        </nav>

        <main className="container m-auto mt-4 px-4"> {children} </main>

        <footer className="flex h-10 bg-red-100 justify-center items-center shodow-inner">
          <p>Copyright &copy; 2022, NextMall</p>
        </footer>
      </div>
    </>
  );
}
